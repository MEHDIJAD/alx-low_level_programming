#include <stdio.h>
/**
 * main - main block
 * Description: a fuction that prints the alphabite in lowercase
 * folled by a new line.
 * Return: void
 */

void print_alphabet(void)
{
   char c = 'a';
   while (c <= 'z')
   
   {
    putchar(c);
    c++;
   } 
   putchar('\n');
   return;
}

